#pragma once
#include "DxLib.h"

// =============================================
// Playerクラス
// プレイヤーの座標・状態・描画を管理するクラス
// =============================================
class Player
{
public:
    // -----------------------------------------
    // プレイヤーの状態
    // -----------------------------------------
    enum class State
    {
        Normal, // 通常状態：ブロックと衝突する
        Shadow  // 影状態：特定のオブジェクトをすり抜ける
    };



private:

    // -----------------------------------------
    // 位置情報
    // -----------------------------------------
    int x;  // X座標（画面上の横位置）
    int y;  // Y座標（画面上の縦位置）


    // -----------------------------------------
    // 見た目
    // -----------------------------------------
    int radius; // 円の半径
    int color;  // 描画色（状態に応じて変化）

    // -----------------------------------------
    // 移動関連
    // -----------------------------------------
    int speed; // 移動速度（1フレームあたりの移動量）

    // -----------------------------------------
    // 状態変数
    // -----------------------------------------
    State state; // 現在のプレイヤー状態

public:
    // =========================================
    // 初期化処理
    // プレイヤーの初期位置・状態を設定する
    // =========================================
    void Initialize();

    // =========================================
    // 更新処理
    // 入力に応じて移動や状態の更新を行う
    // =========================================
    void Update();

    // =========================================
    // 描画処理
    // 現在の状態に応じてプレイヤーを描画する
    // ※内部状態を変更しないため const を付与
    // =========================================
    void Draw() const;

private:
    // =========================================
    // 移動処理
    // 入力をもとに座標を更新する
    // =========================================
    void Move();

    // =========================================
    // 状態切り替え処理
    // 入力に応じて Normal / Shadow を切り替える
    // =========================================
    void ChangeState();

    // =========================================
    // 色更新処理
    // 状態に応じて描画色を変更する
    // =========================================
    void UpdateColor();

public:
    // =========================================
    // 座標取得（まとめて取得）
    // =========================================
    void GetLocation(int& outX, int& outY) const;
};